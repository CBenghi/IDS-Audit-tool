	/// The string value of the UnitType enum of a valid matching unit, 
	/// it starts with "IfcUnitEnum." or "IfcDerivedUnitEnum."
	/// </summary>
	public string UnitTypeEnum { get; }

	/// <summary>
	/// Returns the SI preferred unit.
	/// </summary>
	/// <returns>empty string for measures that do not have expected measures</returns>
	public string GetUnit()
	{
		if (!string.IsNullOrEmpty(Unit))
			return Unit;
		if (Exponents is not null)
			return Exponents.ToUnitSymbol();
		return "";
	}

	/// <summary>
	/// Finsds the SI unit names enum for this measure, if any are available.
	/// </summary>
	/// <param name="filter">leave empty to search for any SI unit enum, otherwise specify a string like `IfcSIUnitName.KELVIN` or `KELVIN`</param>
	/// <returns></returns>
	public bool HasSiUnitEnum(string? filter = null)
	{
		if (filter is null)
			return SiUnitNameEnums.Any();
		return SiUnitNameEnums.Any(x => x.ToUpperInvariant().EndsWith(filter.ToUpperInvariant()));
	}

	/// <inheritdoc/>
	public IUnitInformation? GetParentUnit()
	{
		return null;
	}

	/// <summary>
	/// The range of characters that are valid in SI unit names. This includes the Greek letters mu and omega, and the degree symbol.
	/// </summary>
	public static string SiUnitNameCharactersRange = "a-zA-ZμΩ°";

	/// <summary>
	/// The range of characters that reasonable in unit names. This includes the SI unit names expanded with double and single quotes for feet and inch.
	/// </summary>
	public static string BroadUnitNameCharactersRange = """a-zA-ZμΩ°"'""";

	/// <summary>
	/// Standard regex catcher for SI unit components, such as mm, m2, mm-2 or Gy 
	/// </summary>
	public static Regex SiUnitComponentMatcher => new(@"^[\s]*(?<chars>[a-zA-ZμΩ°]+)[\s]*(?<pow>[+-]?[\d²³]*)[\s]*$", RegexOptions.Compiled);

	/// <summary>
	/// Standard regex catcher for unit components, such as mm, m2, or Gy, but expanded to match " and ' for inch and feet
	/// </summary>
	public static Regex BroadUnitComponentMatcher => new("""^[\s]*(?<chars>[a-zA-ZμΩ°"']+)[\s]*(?<pow>[+-]?[\d²³]*)[\s]*$""", RegexOptions.Compiled);

	/// <summary>
	/// Tries to parse a string into a SI unit name, and returns the relevant exponents and SI prefix multiplier.
	/// </summary>
	public static bool TryGetSIUnitFromString(string unitName, [NotNullWhen(true)] out DimensionalExponents? exponents, out IfcConversionUnitInformation.SiPrefix siPrefix, out int pow)
	{
		var m = SiUnitComponentMatcher.Match(unitName);
		if (!m.Success)
		{
			exponents = null;
			siPrefix = IfcConversionUnitInformation.SiPrefix.NONE;
			pow = 0;
			return false;
		}
		var tUnit = GetUnit(m.Groups["chars"].Value);
		exponents = DimensionalExponents.FromString(tUnit.exponents);
		siPrefix = tUnit.siPrefix;
		pow = GetExponent(m.Groups["pow"].Value);
		return exponents is not null;
	}

	/// <summary>
	/// Tries to parse a string into a SI unit name, and returns the relevant components.
	/// </summary>
	public static bool TryCleanSIUnitFromString(string unitName, [NotNullWhen(true)] out string? bareUnit, out IfcConversionUnitInformation.SiPrefix siPrefix, out int pow)
	{
		var m = SiUnitComponentMatcher.Match(unitName);
		if (!m.Success)
		{
			bareUnit = null;
			siPrefix = IfcConversionUnitInformation.SiPrefix.NONE;
			pow = 0;
			return false;
		}
		var tUnit = GetUnit(m.Groups["chars"].Value);
		bareUnit = tUnit.bareUnit;
		siPrefix = tUnit.siPrefix;
		pow = GetExponent(m.Groups["pow"].Value);
		return !string.IsNullOrEmpty(bareUnit);
	}

	private static int GetExponent(string exponentString)
	{
		if (string.IsNullOrWhiteSpace(exponentString))
			return 1;
		exponentString = exponentString.Replace("²", "2");
		exponentString = exponentString.Replace("³", "3");
		if (int.TryParse(exponentString, out var pow))
			return pow;
		return 0;
	}

	/// <summary>
	/// Gets the preferred measure for a given unit name.
	/// </summary>
	public static IfcMeasureInformation? GetPreferredMeasure(string unitName)
	{
		if (!TryGetSIUnitFromString(unitName, out var exponents, out var siPrefix, out var pow))
			return null;
		return GetPreferredMeasure(exponents);
	}

	/// <summary>
	/// Gets the preferred measure for a given dimensional exponent.
	/// </summary>
	public static IfcMeasureInformation? GetPreferredMeasure(DimensionalExponents exp)
	{
		var measures = IdsLib.IfcSchema.SchemaInfo.AllMeasureInformation;
		var match = measures.Where(x => x.Exponents.Equals(exp)).ToList();
		if (match.Count == 1)
			return match.First();
		match = match.Where(x => DimensionalExponents.UnitMeasures.Contains(x.IfcMeasure)).ToList();
		if (match.Count == 1)
			return match.First();
		return null;
	}

